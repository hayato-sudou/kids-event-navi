/** マスタからスナップショット（Task[]）を生成する */
export function getDefaultTasks(eventKey: EventKey): Task[] {
  return TASK_MASTER[eventKey].map((item) => ({
    id: `default-${item.key}`,
    label: item.label,
    description: item.description,
    checked: false,
  }));
}